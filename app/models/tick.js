//=============================================================================
//  Module Dependencies
//=============================================================================

var mongoose = require('mongoose'),
    Schema = mongoose.Schema;

//=============================================================================
//  Tick Schema
//=============================================================================

var TickSchema = new Schema({
    time: {type: Date, default: Date.now},
    securities: [{
        symbol: String,
        ask: Number,
        bid: Number,
        last: Number,
        error: String
    }]
});

//=============================================================================
//  Finalize
//=============================================================================

mongoose.model('Tick', TickSchema);
